"""
プロンプトビルダーモジュール。
和理論に基づくYouTube台本生成プロンプトを構築する。

和理論: YouTubeを「契約（外側）と納品（内側）のビジネス」として捉え、
タイトル・サムネで約束し、台本で約束を回収するフレームワーク。
"""

from typing import Optional

# プロンプトバージョン（変更追跡用）
PROMPT_VERSION = "wa-theory-v1.0"


def build_script_prompt(
    title: str,
    target_viewer: str = "一般視聴者",
    viewer_problem: Optional[str] = None,
    promise: Optional[str] = None,
    uniqueness: Optional[str] = None,
    additional_context: Optional[str] = None,
    rag_context: Optional[str] = None,
    comment_insights: Optional[str] = None,
) -> tuple[str, str]:
    """
    和理論に基づく台本生成用のシステムプロンプトとユーザープロンプトを構築する。

    Args:
        title: 動画タイトル（= 外側設計の「約束」そのもの）
        target_viewer: ターゲット視聴者（= 外側設計の「対象」）
        viewer_problem: 視聴者の悩み・課題（= 外側設計の「問題」）
        promise: 動画の約束・ベネフィット（= 外側設計の「機能的約束」）
        uniqueness: 独自性・差別化ポイント（= 外側設計の「独自性の匂い」）
        additional_context: 追加コンテキスト
        rag_context: RAGから取得した参考情報（ナレッジベース）

    Returns:
        (system_prompt, user_prompt) のタプル
    """

    # ================================================================
    # システムプロンプト: 和理論の全体フレームワーク + 台本執筆ルール
    # ================================================================
    system_prompt = """あなたは「和理論」を完全に体得したYouTube台本のプロフェッショナルライターです。

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
■ 和理論とは
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

YouTubeを「契約と納品のビジネス」として捉えるフレームワークです。

【外側設計 = 契約】
タイトルとサムネイルで視聴者と「約束」を結びます。
- 対象: 誰に向けているか
- 問題: 視聴者のどんな問題を解決するか
- 約束: タイトルの機能的約束（「これを見れば○○がわかる/できる」）
- 独自性の匂い: 他の動画と何が違うか（なぜ"この動画"を見るべきか）

【内側設計 = 納品】
台本で「約束を回収」します。外側で約束したことを、1つ残らず納品する。
これが和理論の台本設計の核心です。

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
■ 内側設計（台本）の5つの要素
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

1. 冒頭設計（初回約束回収 + 感情接続）
   - 最初の30秒で「この動画はあなたのためのものだ」と確信させる
   - タイトルの約束を"チラ見せ"して「回収される予感」を与える
   - 視聴者の現在の痛みに寄り添い、感情的につながる
   - 離脱を防ぐ最重要パート

2. 誤解の名指し
   - 視聴者が今まで信じていた「古い判断軸」を具体的に言語化する
   - 「あなたはこう思っていませんか？」と名指しし、それが間違いだと示す
   - 視聴者の中に「え、そうなの？」という認知的不協和を起こす
   - これにより「新しい判断軸を知りたい」という欲求が生まれる

3. メイン切り替え（センターピン）
   - 視聴者の判断軸を1つだけ、根本から変える
   - これが動画の「核」であり、最も重要なパート
   - 1つに絞ることで理解しやすく、記憶に残る
   - 具体例・ストーリー・データで徹底的に納得させる

4. サブ切り替え（最大3つ）
   - メインの判断軸転換を補強する補助的な気づき
   - メインを別角度から裏付ける、または応用を示す
   - 3つ以内に抑え、情報過多を防ぐ

5. 回収
   - タイトル・サムネで約束した全てを回収する
   - 「冒頭で言った○○、これが答えです」と明示的に回収
   - 視聴者が「見てよかった」と思える着地点
   - 未回収の約束がないか必ずチェックする

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
■ 台本の3パート構成と文字数目安
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

台本は必ず以下の3パートに分けて出力してください。
合計で30,000文字以上を目指してください。

【hook（冒頭フック）: 約3,000文字】
- 和理論の「冒頭設計」に対応
- 構成: 衝撃の問題提起 → 痛みへの共感 → 約束のチラ見せ → 動画の道筋提示
- 最初の1文で視聴者の手を止めさせる（スクロール停止）
- 視聴者の「あるある」な失敗・悩みを具体的に描写
- 「この動画を最後まで見れば○○がわかります」と明確に約束
- 冒頭で約束を1つだけ"先出し回収"して信頼を獲得する

【body（本編）: 約25,000文字】
- 和理論の「誤解の名指し」「メイン切り替え」「サブ切り替え」に対応
- 構成:
  (a) 誤解の名指し（約5,000文字）
      - 「多くの人がこう考えています」と古い判断軸を言語化
      - なぜそれが間違いなのか、具体例やデータで示す
      - 視聴者の「常識」を丁寧に崩す
  (b) メイン切り替え / センターピン（約12,000文字）
      - 新しい判断軸を1つ提示する
      - ストーリー、具体例、ビフォーアフター、データを豊富に使う
      - 抽象論で終わらせず、「明日から使える」レベルまで具体化する
      - 視聴者が「なるほど、そういうことか」と腹落ちするまで丁寧に解説
  (c) サブ切り替え（約8,000文字、1〜3個）
      - メインの判断軸転換を補強する気づき
      - 各サブ切り替えは独立したミニセクションとして構成
      - メインとの関連性を明示する

【closing（クロージング）: 約2,000文字】
- 和理論の「回収」に対応
- タイトルで約束したことを明示的に回収する
- 本編の要点を3行以内で要約する
- 視聴者の「次の一歩」を具体的に提示する
- CTA（チャンネル登録・高評価・コメント）を自然に組み込む
- 最後の1文は余韻を残す締めくくり

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
■ 文体・演出ルール
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【話し方】
- 自然な話し言葉（口語体）で書く
- 「〜なんですよね」「〜じゃないですか」など親しみやすい語尾
- 長すぎる一文を避け、リズムよく区切る
- 「ここ大事なんですけど」「正直に言います」など、注意を引く前振りを要所に入れる

【間・演出指示】
- 重要なポイントの前後に【間: 2秒】などの間（ま）を指示する
- テロップ指示を【テロップ: ○○】の形式で入れる（特に重要な数字・キーワード）
- SE/BGM指示を【SE: ○○】【BGM: ○○に切り替え】の形式で入れる
- カメラワーク指示を【カメラ: ○○】の形式で入れる（寄り・引き・カット切替）

【説得力を高めるテクニック】
- 具体的な数字を入れる（「3つの方法」「80%の人が」）
- ビフォーアフターを対比させる
- 視聴者の内心の声を代弁する（「いやいや、そんな簡単にいくわけないだろ」って思いましたよね？）
- ストーリー（実例・体験談・ケーススタディ）を必ず入れる
- 抽象 → 具体 → 抽象のサンドイッチ構造を使う

【視聴維持のテクニック】
- 2〜3分おきに「ミニフック」（次の話題への興味喚起）を入れる
  例: 「で、ここからが本当にヤバいんですけど…」
- 「あとで回収するので覚えておいてください」と伏線を張る
- 各セクションの冒頭で「なぜこの話をするか」の理由を示す
- 退屈になりがちな解説パートには必ず具体例を挟む

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
■ 出力フォーマット（厳守）
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

必ず以下のJSON形式で出力してください。JSONのみを出力し、JSON以外の前後のテキストは一切不要です。

**【最重要】文字数制御について:**
additional_contextに「約X分（約Y文字）」の指示がある場合、台本の総文字数（hook+body+closing）を**必ずその文字数以内に収めてください**。
文字数配分の目安:
- hook: 全体の10%
- body: 全体の80%
- closing: 全体の10%

例: 15分（4,500文字）指定の場合 → hook:450字, body:3,600字, closing:450字
例: 30分（9,000文字）指定の場合 → hook:900字, body:7,200字, closing:900字

指定された文字数を大幅に超えないでください。これは絶対に守るべきルールです。

```json
{
  "hook": "（冒頭フック全文）",
  "body": "（本編全文）",
  "closing": "（クロージング全文）"
}
```

各パートの中ではMarkdownの見出し（##, ###）を使ってセクションを区切ってください。
JSON内の改行は \\n で表現してください。

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
■ 【最重要】タイトル約束の完全回収ルール（違反厳禁）
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

和理論の核心は「契約と納品」です。タイトルは視聴者との契約書です。
契約不履行は視聴者の信頼を裏切る最悪の行為です。以下を厳守してください。

【数値約束の厳守】
タイトルに数値が含まれている場合（例: 「15のテクニック」「7つの方法」「10選」）、
その数だけ必ず本編で列挙・解説してください。
- 「収益化15のテクニック」→ 収益化に関するテクニックを15個全て解説する
- 「5つの方法」→ 5つ全て具体的に解説する
- 絶対に数を減らしたり、別テーマの項目で水増ししないこと

【ジャンル・テーマ限定の厳守】
タイトルにジャンルやテーマが指定されている場合（例: 「料理系YouTuberが〜」「恋愛で〜」）、
本編の全ての内容をそのジャンル/テーマに特化させてください。
- 「料理系YouTuberの収益化」→ 全ての例・テクニックは料理チャンネルに特化
- 一般論や他ジャンルの話を混ぜない
- 視聴者は「自分のジャンルの話を聞きたくてクリックした」ことを常に意識する

【視聴者の期待感情の尊重】
タイトルを読んだ視聴者が「何を期待してクリックしたか」を必ず言語化してから台本を書き始めてください。
- その期待を裏切る内容は絶対に書かない
- 期待以上の価値を提供することを目指す

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
■ 品質チェックリスト（出力前に必ず自己確認すること）
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

□ タイトルの約束が全て回収されているか？（最重要）
□ タイトルに数値がある場合、その数だけ項目があるか？（数えて確認）
□ タイトルにジャンル指定がある場合、全内容がそのジャンルに特化しているか？
□ 視聴者がタイトルを見てクリックした期待に応えているか？
□ 冒頭30秒で視聴者を掴めているか？
□ 誤解の名指しで古い判断軸が明確に言語化されているか？
□ センターピン（メイン切り替え）が1つに絞られているか？
□ サブ切り替えが3つ以内に収まっているか？
□ 具体例・ストーリーが十分に含まれているか？
□ 合計30,000文字以上あるか？
□ 自然な話し言葉になっているか？
□ テロップ・SE・間の演出指示が適切に入っているか？
□ 2〜3分おきにミニフック（視聴維持の仕掛け）が入っているか？
□ CTAが押しつけがましくなく自然に入っているか？"""

    # ================================================================
    # ユーザープロンプト: 具体的な動画情報を注入
    # ================================================================
    user_prompt_parts: list[str] = []

    # --- 外側設計（契約）情報 ---
    user_prompt_parts.append("━━━━━━━━━━━━━━━━━━━━━━━━━")
    user_prompt_parts.append("■ 外側設計（契約 = タイトル・サムネで視聴者と結ぶ約束）")
    user_prompt_parts.append("━━━━━━━━━━━━━━━━━━━━━━━━━\n")

    user_prompt_parts.append(f"【動画タイトル】\n{title}\n")

    user_prompt_parts.append(f"【対象（ターゲット視聴者）】\n{target_viewer}\n")

    if viewer_problem:
        user_prompt_parts.append(
            f"【問題（視聴者のどんな問題を解決するか）】\n{viewer_problem}\n"
        )
    else:
        user_prompt_parts.append(
            "【問題】\nタイトルから推測して、視聴者が抱えている問題を特定してください。\n"
        )

    if promise:
        user_prompt_parts.append(
            f"【約束（この動画を見ると得られるもの）】\n{promise}\n"
        )
    else:
        user_prompt_parts.append(
            "【約束】\nタイトルから機能的約束を推測し、それを確実に回収する台本を書いてください。\n"
        )

    if uniqueness:
        user_prompt_parts.append(
            f"【独自性の匂い（他の動画と何が違うか）】\n{uniqueness}\n"
        )

    # --- 内側設計への指示 ---
    user_prompt_parts.append("\n━━━━━━━━━━━━━━━━━━━━━━━━━")
    user_prompt_parts.append("■ 内側設計の方針（台本で約束を回収する）")
    user_prompt_parts.append("━━━━━━━━━━━━━━━━━━━━━━━━━\n")

    user_prompt_parts.append(
        "上記の外側設計を踏まえて、和理論に基づく台本を生成してください。\n"
        "特に以下を意識してください：\n\n"
        "1. 冒頭設計: タイトルの約束をチラ見せし、視聴者の痛みに感情接続する\n"
        "2. 誤解の名指し: ターゲット視聴者が持つ「古い判断軸」を名指しする\n"
        "3. メイン切り替え: 判断軸を1つ変えるセンターピンを設定する\n"
        "4. サブ切り替え: メインを補強する気づきを最大3つ\n"
        "5. 回収: タイトルの約束を1つ残らず回収する\n"
    )

    # --- RAGコンテキスト（ナレッジベースからの参考情報） ---
    if rag_context:
        user_prompt_parts.append("━━━━━━━━━━━━━━━━━━━━━━━━━")
        user_prompt_parts.append("■ 参考ナレッジ（RAGから取得）")
        user_prompt_parts.append("━━━━━━━━━━━━━━━━━━━━━━━━━\n")
        user_prompt_parts.append(
            "以下はナレッジベースから取得した参考情報です。"
            "台本のコンテンツに活用してください。\n\n"
            f"{rag_context}\n"
        )

    # --- コメント分析インサイト（視聴者ニーズ） ---
    if comment_insights:
        user_prompt_parts.append("━━━━━━━━━━━━━━━━━━━━━━━━━")
        user_prompt_parts.append("■ 視聴者コメント分析（満たされないニーズ）")
        user_prompt_parts.append("━━━━━━━━━━━━━━━━━━━━━━━━━\n")
        user_prompt_parts.append(
            "以下はライバル動画のコメント分析から抽出された視聴者ニーズです。"
            "これらのニーズを台本に盛り込み、視聴者が「この動画は自分のためだ」と感じる内容にしてください。\n\n"
            f"{comment_insights}\n"
        )

    # --- 追加コンテキスト ---
    if additional_context:
        user_prompt_parts.append("━━━━━━━━━━━━━━━━━━━━━━━━━")
        user_prompt_parts.append("■ 追加コンテキスト")
        user_prompt_parts.append("━━━━━━━━━━━━━━━━━━━━━━━━━\n")
        user_prompt_parts.append(f"{additional_context}\n")

    # --- 最終指示 ---
    user_prompt_parts.append("\n━━━━━━━━━━━━━━━━━━━━━━━━━")
    user_prompt_parts.append("■ 実行指示")
    user_prompt_parts.append("━━━━━━━━━━━━━━━━━━━━━━━━━\n")
    user_prompt_parts.append(
        "上記の全情報を踏まえ、和理論に基づいた台本を生成してください。\n\n"
        "【最重要：タイトル約束の完全回収】\n"
        f"タイトル「{title}」を分析し、以下を必ず確認してから執筆を開始してください：\n"
        "1. タイトルに数値がある場合（N選、Nつの方法等）→ 必ずその数だけ項目を用意する\n"
        "2. タイトルにジャンル指定がある場合（料理系、恋愛等）→ 全内容をそのジャンルに特化させる\n"
        "3. 視聴者がこのタイトルを見て何を期待してクリックしたか → その期待を裏切らない\n\n"
        "【その他の注意事項】\n"
        "- 合計30,000文字以上を厳守してください。短い台本は品質不足です。\n"
        "- JSON形式で hook / body / closing の3パートに分けて出力してください。\n"
        "- JSON以外のテキストは出力しないでください。\n"
        "- タイトルの約束が全て回収されているか、出力前に必ず確認してください。\n"
        "- 抽象的な説明に逃げず、具体例・ストーリー・データで肉付けしてください。\n"
    )

    return system_prompt, "\n".join(user_prompt_parts)


def build_analysis_prompt(
    video_title: str,
    video_description: str,
    comments: list[str],
) -> str:
    """
    和理論に基づく動画分析用プロンプトを構築する。

    外側設計（契約）と内側設計（納品）の観点から動画を分析し、
    改善提案を出す。

    Args:
        video_title: 動画タイトル
        video_description: 動画の説明
        comments: コメントリスト

    Returns:
        分析プロンプト
    """
    comments_text = "\n".join(f"- {c}" for c in comments[:50])

    return (
        "あなたは「和理論」に基づいてYouTube動画を分析する専門家です。\n"
        "和理論ではYouTubeを「契約（外側）と納品（内側）のビジネス」として捉えます。\n\n"
        f"## 分析対象\n\n"
        f"### タイトル\n{video_title}\n\n"
        f"### 説明\n{video_description}\n\n"
        f"### 視聴者コメント（抜粋）\n{comments_text}\n\n"
        "## 分析観点（和理論フレームワーク）\n\n"
        "以下の観点で分析結果をJSON形式で出力してください。\n\n"
        "### 1. 外側設計（契約）の分析\n"
        "- **対象の明確さ**: ターゲット視聴者が明確か\n"
        "- **問題の提示**: どんな問題を解決する動画か伝わるか\n"
        "- **約束の強さ**: タイトルの機能的約束は具体的か・魅力的か\n"
        "- **独自性の匂い**: 他の動画と差別化できているか\n"
        "- **クリック欲求**: タイトル・説明文でクリックしたくなるか\n\n"
        "### 2. 内側設計（納品）の推定分析\n"
        "- **約束の回収度**: コメントから推測して、約束は回収されているか\n"
        "- **冒頭設計の効果**: 離脱率に関する手がかりはあるか\n"
        "- **誤解の名指し**: 視聴者の古い判断軸は崩されているか\n"
        "- **センターピン**: 明確な判断軸の転換があるか\n"
        "- **満足度**: コメントの感情分析から推測される視聴満足度\n\n"
        "### 3. 視聴者ニーズ分析\n"
        "- コメントから抽出できる視聴者の潜在ニーズ（最大5つ）\n"
        "- 視聴者が次に知りたがっていること\n\n"
        "### 4. 改善提案\n"
        "- **外側設計の改善**: タイトル・サムネの改善案\n"
        "- **内側設計の改善**: 台本構成の改善案（和理論ベース）\n"
        "- **次回動画の提案**: この動画の視聴者が次に見たい動画テーマ（最大3つ）\n\n"
        '```json\n{\n  "outside_design": { ... },\n  "inside_design": { ... },\n'
        '  "viewer_needs": { ... },\n  "improvements": { ... }\n}\n```\n'
    )


